"""1 Найти наибольший элемент списка, который делиться на 2 без остатка и 
вывести его на экран. """
print("=== ЗАДАНИЕ 1 ===")
print("Найти наибольший элемент списка, который делится на 2 без остатка")

# Ввод списка
n1 = int(input("Введите количество элементов в списке: "))
arr1 = []

for i in range(n1):
    arr1.append(int(input(f"Введите элемент {i + 1}: ")))

# Поиск наибольшего чётного элемента
max_even = None
for num in arr1:
    if num % 2 == 0:  # Проверка на чётность
        if max_even is None or num > max_even:
            max_even = num

# Вывод результата
if max_even is not None:
    print(f"Наибольший чётный элемент: {max_even}")
else:
    print("В списке нет чётных элементов.")

print()
"""2. Дан одномерный массив целого типа. Получить другой массив, состоящий 
только из четных чисел исходного массива, меньше 10, или сообщить, что таких 
чисел нет. Полученный массив вывести в порядке возрастания элементов"""
print("=== ЗАДАНИЕ 2 ===")
print("Получить массив из чётных чисел меньше 10, отсортированный по возрастанию")

# Ввод исходного массива
n2 = int(input("Введите количество элементов в массиве: "))
arr2 = []

for i in range(n2):
    arr2.append(int(input(f"Введите элемент {i + 1}: ")))

# Формирование нового массива
new_arr = [num for num in arr2 if num % 2 == 0 and num < 10]

# Вывод результата
if new_arr:
    new_arr.sort()  # Сортировка по возрастанию
    print("Чётные числа меньше 10:", new_arr)
else:
    print("Нет чётных чисел меньше 10.")
